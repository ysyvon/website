  function resolve(arg) {
    arg = (arg || "").trim(); if (!arg) return null;
    if (byNumber[arg]) return byNumber[arg].key;
    const n = norm(arg);
    if (byKey[n]) return n;
    let hit = DATA.find(function (e) { return norm(e.meta.code) === n; });
    if (hit) return hit.key;
    hit = DATA.find(function (e) { return e.key.indexOf(n) === 0; }) ||
          DATA.find(function (e) { return e.key.indexOf(n) !== -1; });
    return hit ? hit.key : null;
  }

  function run(raw) {
    const line = raw.trim(); if (!line) return;
    history.push(line); hpos = history.length;
    const sp = line.indexOf(" ");
    const verb = (sp === -1 ? line : line.slice(0, sp)).toLowerCase();
    const arg = sp === -1 ? "" : line.slice(sp + 1);

    switch (verb) {
      case "help": case "?": showHelp(); break;
      case "about": case "notice": showAbout(); break;
      case "index": case "list": case "ls": case "home": case "menu": case "back":
        cur = null; showIndex(); break;
      case "open": case "view": case "read": case "cat": case "access": {
        const k = resolve(arg);
        if (k) openEntry(k);
        else { clearScreen(); printHTML('<div class="alert">&gt; no record: "' + escapeHtml(arg) + '". Try <span class="hot">index</span>.</div>'); }
        break;
      }
      case "next": case "n": case "fwd": pageStep(1); break;
      case "prev": case "p": case "back1": pageStep(-1); break;
      case "map": case "chart": case "survey": showMap(norm(arg)); break;
      case "search": case "grep": case "find": doSearch(arg); break;
      case "clear": case "cls": clearScreen(); break;
      case "whoami": clearScreen(); printHTML('<div class="dim">operator // clearance level 3</div>'); break;
      case "exit": case "quit": case "logout":
        clearScreen(); printHTML('<div class="dim">&gt; this door does not close behind you.</div>'); break;
      default: {
        const k = resolve(line);
        if (k) openEntry(k);
        else { clearScreen(); printHTML('<div class="alert">&gt; unknown command: "' + escapeHtml(verb) + '". Type <span class="hot">help</span>.</div>'); }
      }
    }
  }

