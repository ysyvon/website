  function revealHTML(html, delay, toBottom) {
    delay = delay || 30;
    const wrap = document.createElement("div"); wrap.innerHTML = html;
    const kids = Array.from(wrap.childNodes);
    const host = document.createElement("div"); out.appendChild(host);
    let i = 0; revealing = true;
    skipFn = function () { while (i < kids.length) { host.appendChild(kids[i]); i++; } if (toBottom) scrollBottom(); };
    (function step() {
      if (i >= kids.length) { revealing = false; revealTimer = null; return; }
      host.appendChild(kids[i]); i++;
      if (toBottom) scrollBottom();
      revealTimer = setTimeout(step, delay);
    })();
  }

