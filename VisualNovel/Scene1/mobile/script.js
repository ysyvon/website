const sceneImage = document.getElementById("scene-image");
const reel = document.querySelector(".reel");
const dialogueSpeaker = document.getElementById("dialogue-speaker");
const dialogueLine = document.getElementById("dialogue-line");
const progressCurrent = document.getElementById("progress-current");
const progressTotal = document.getElementById("progress-total");
const startOverlay = document.getElementById("start-overlay");
const endOverlay = document.getElementById("end-overlay");
const startButton = document.getElementById("start-button");
const replayButton = document.getElementById("replay-button");

const TYPE_MS = 38;

let currentFrame = -1;
let started = false;
let typingTimer = null;
let isTyping = false;
let fullLineText = "";

progressTotal.textContent = String(frames.length).padStart(2, "0");

function clearTyping() {
  if (typingTimer) {
    window.clearTimeout(typingTimer);
    typingTimer = null;
  }
}

function visibleTextLength(html) {
  const template = document.createElement("template");
  template.innerHTML = html;
  return template.content.textContent.length;
}

function renderLineMarkup(html, visibleCharacters = Infinity) {
  const template = document.createElement("template");
  template.innerHTML = html;
  const textNodes = document.createTreeWalker(
    template.content,
    NodeFilter.SHOW_TEXT
  );
  let remaining = visibleCharacters;
  let node = textNodes.nextNode();

  while (node) {
    const text = node.textContent;
    node.textContent = text.slice(0, Math.max(0, remaining));
    remaining -= text.length;
    node = textNodes.nextNode();
  }

  dialogueLine.replaceChildren(template.content.cloneNode(true));
}

function finishTyping() {
  clearTyping();
  renderLineMarkup(fullLineText);
  isTyping = false;
  dialogueLine.classList.remove("typing");
}

function typeLine(text) {
  clearTyping();
  fullLineText = text;
  dialogueLine.replaceChildren();
  dialogueLine.classList.add("typing");
  isTyping = true;
  const length = visibleTextLength(text);
  let index = 0;

  function step() {
    index += 1;
    renderLineMarkup(text, index);

    if (index >= length) {
      isTyping = false;
      typingTimer = null;
      dialogueLine.classList.remove("typing");
      return;
    }

    typingTimer = window.setTimeout(step, TYPE_MS);
  }

  step();
}

function renderFrame(index) {
  const frame = frames[index];
  sceneImage.src = frame.background;
  sceneImage.alt = `${frame.speaker} scene`;
  sceneImage.classList.toggle("reisz-frame", frame.speaker === "Director Reisz");
  dialogueSpeaker.textContent = frame.speaker;
  dialogueLine.classList.toggle("compact", visibleTextLength(frame.line) > 135);
  progressCurrent.textContent = String(index + 1).padStart(2, "0");
  typeLine(frame.line);
}

function beginPlayback() {
  clearTyping();
  started = true;
  currentFrame = 0;
  startOverlay.classList.add("hidden");
  endOverlay.classList.add("hidden");
  renderFrame(currentFrame);
}

function advanceFrame() {
  if (!started) {
    return;
  }

  if (isTyping) {
    finishTyping();
    return;
  }

  if (currentFrame < frames.length - 1) {
    currentFrame += 1;
    renderFrame(currentFrame);
    return;
  }

  started = false;
  currentFrame = -1;
  endOverlay.classList.remove("hidden");
}

startButton.addEventListener("click", (event) => {
  event.stopPropagation();
  beginPlayback();
});

replayButton.addEventListener("click", (event) => {
  event.stopPropagation();
  beginPlayback();
});

reel.addEventListener("click", advanceFrame);

document.addEventListener("keydown", (event) => {
  if (event.key !== " " && event.key !== "Enter") {
    return;
  }

  event.preventDefault();

  if (!started) {
    beginPlayback();
    return;
  }

  advanceFrame();
});
