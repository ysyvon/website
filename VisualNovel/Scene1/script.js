const beginOverlay = document.getElementById("begin-overlay");
const beginButton = document.getElementById("begin-button");
const scene = document.getElementById("scene");
const sceneBackground = document.getElementById("scene-background");
const sceneSprite = document.getElementById("scene-sprite");
const dialogueSpeaker = document.getElementById("dialogue-speaker");
const dialogueLine = document.getElementById("dialogue-line");

let currentFrame = -1;
let started = false;
let typingTimer = null;
let isTyping = false;
let fullLineText = "";
const TYPE_MS = 18;

function clearTyping() {
  if (typingTimer) {
    window.clearTimeout(typingTimer);
    typingTimer = null;
  }
}

function renderLineMarkup(html, visibleCharacters = Infinity) {
  const template = document.createElement("template");
  template.innerHTML = html;
  const textNodes = document.createTreeWalker(
    template.content,
    NodeFilter.SHOW_TEXT
  );
  let remaining = visibleCharacters;
  let node = textNodes.nextNode();

  while (node) {
    const text = node.textContent;
    node.textContent = text.slice(0, Math.max(0, remaining));
    remaining -= text.length;
    node = textNodes.nextNode();
  }

  dialogueLine.replaceChildren(template.content.cloneNode(true));
}

function finishTyping() {
  clearTyping();
  renderLineMarkup(fullLineText);
  isTyping = false;
  dialogueLine.classList.remove("typing");
}

function typeLine(text) {
  clearTyping();
  fullLineText = text;
  dialogueLine.replaceChildren();
  isTyping = true;
  dialogueLine.classList.add("typing");
  const template = document.createElement("template");
  template.innerHTML = text;
  const visibleLength = template.content.textContent.length;
  let index = 0;

  function step() {
    index += 1;
    renderLineMarkup(text, index);

    if (index >= visibleLength) {
      isTyping = false;
      typingTimer = null;
      dialogueLine.classList.remove("typing");
      return;
    }

    typingTimer = window.setTimeout(step, TYPE_MS);
  }

  step();
}

function renderFrame(index) {
  const frame = frames[index];
  sceneBackground.src = frame.background;
  sceneBackground.alt = `${frame.speaker} background`;
  sceneBackground.classList.toggle("sabine-frame", frame.speaker === "Director Reisz");
  dialogueSpeaker.textContent = frame.speaker;
  typeLine(frame.line);
  sceneSprite.removeAttribute("src");
  sceneSprite.alt = "";
  sceneSprite.className = "scene-sprite hidden";
}

function startScene() {
  started = true;
  currentFrame = 0;
  beginOverlay.classList.add("hidden");
  renderFrame(currentFrame);
}

function advanceFrame() {
  if (!started) {
    return;
  }

  if (isTyping) {
    finishTyping();
    return;
  }

  if (currentFrame < frames.length - 1) {
    currentFrame += 1;
    renderFrame(currentFrame);
    return;
  }

  started = false;
  currentFrame = -1;
  beginOverlay.classList.remove("hidden");
}

beginButton.addEventListener("click", (event) => {
  event.stopPropagation();
  startScene();
});

scene.addEventListener("click", advanceFrame);

document.addEventListener("keydown", (event) => {
  const inspectShortcut =
    event.key === "F12" ||
    ((event.ctrlKey || event.metaKey) && event.shiftKey && ["I", "J", "C"].includes(event.key.toUpperCase())) ||
    ((event.ctrlKey || event.metaKey) && event.key.toUpperCase() === "U");

  if (inspectShortcut) {
    event.preventDefault();
    event.stopPropagation();
    return;
  }

  if (event.key !== " " && event.key !== "Enter") {
    return;
  }

  event.preventDefault();

  if (!started) {
    startScene();
    return;
  }

  advanceFrame();
});

document.addEventListener("contextmenu", (event) => {
  event.preventDefault();
});

document.addEventListener("dragstart", (event) => {
  event.preventDefault();
});

document.addEventListener("selectstart", (event) => {
  event.preventDefault();
});
